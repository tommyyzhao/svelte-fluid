<h2>FluidEngine (Advanced)</h2>

<p>
	For advanced users who need framework-agnostic fluid simulation, the <code>FluidEngine</code>
	class is available as a direct import. It is the WebGL engine that powers the
	<code>&lt;Fluid&gt;</code> component, but it has no Svelte dependency.
</p>

<pre><code>import {LB} FluidEngine {RB} from 'svelte-fluid/engine/FluidEngine';</code></pre>

<p>
	The engine is constructed with a <code>canvas</code> element and a <code>config</code> object
	matching the <code>FluidConfig</code> interface. It exposes the same <code>splat()</code>,
	<code>randomSplats()</code>, <code>pause()</code>, <code>resume()</code>, and
	<code>isPaused</code> API as <code>FluidHandle</code>, plus additional methods:
</p>

<ul>
	<li><code>setConfig(config)</code> — update configuration at runtime (respects the 4-bucket system)</li>
	<li><code>dispose()</code> — free all GPU resources (textures, framebuffers, programs). The WebGL context is left intact for potential reuse.</li>
</ul>

<p>
	Architecture invariant: the engine never imports Svelte. It is fully framework-agnostic and
	could be used with React, vanilla JS, or any other framework. The Svelte component is a thin
	wrapper that handles lifecycle, sizing, and prop reactivity.
</p>
