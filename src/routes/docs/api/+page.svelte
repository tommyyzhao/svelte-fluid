<h2>FluidHandle Methods</h2>

<h3>splat(x, y, dx, dy, color)</h3>

<p>Inject a single splat at the given position with velocity and color.</p>

<table>
	<thead>
		<tr>
			<th>Parameter</th>
			<th>Type</th>
			<th>Description</th>
		</tr>
	</thead>
	<tbody>
		<tr>
			<td><code>x</code></td>
			<td><code>number</code></td>
			<td>X position in normalized coords (0 = left, 1 = right)</td>
		</tr>
		<tr>
			<td><code>y</code></td>
			<td><code>number</code></td>
			<td>Y position in normalized coords (0 = bottom, 1 = top)</td>
		</tr>
		<tr>
			<td><code>dx</code></td>
			<td><code>number</code></td>
			<td>X velocity (raw value, <strong>not</strong> scaled by <code>splatForce</code>)</td>
		</tr>
		<tr>
			<td><code>dy</code></td>
			<td><code>number</code></td>
			<td>Y velocity (raw value, <strong>not</strong> scaled by <code>splatForce</code>)</td>
		</tr>
		<tr>
			<td><code>color</code></td>
			<td><code>RGB</code></td>
			<td>Color in 0-1 linear range. Values above 1 are valid HDR and appear as bloom highlights.</td>
		</tr>
	</tbody>
</table>

<pre><code>// Inject a bright red splat at the center moving rightward
fluidRef.handle.splat(0.5, 0.5, 500, 0, {LB} r: 1.5, g: 0.1, b: 0.1 {RB});</code></pre>

<p>
	Velocity values are in pixels per frame and written directly to the splat shader uniform.
	Unlike pointer-driven splats, these are <strong>not</strong> multiplied by <code>splatForce</code>.
	Pick the absolute magnitude you want. Typical values range from 100 (gentle) to 1000 (forceful).
</p>

<h3>randomSplats(count)</h3>

<p>Push N additional random splats onto the splat queue. Colors are generated by the engine's internal <code>generateColor</code> function.</p>

<table>
	<thead>
		<tr>
			<th>Parameter</th>
			<th>Type</th>
			<th>Description</th>
		</tr>
	</thead>
	<tbody>
		<tr>
			<td><code>count</code></td>
			<td><code>number</code></td>
			<td>Number of random splats to inject</td>
		</tr>
	</tbody>
</table>

<pre><code>// Add 10 random splats
fluidRef.handle.randomSplats(10);</code></pre>

<h3>pause()</h3>

<p>
	Stop the animation loop. The WebGL context stays alive but no frames are requested. Call
	<code>resume()</code> to restart. Idempotent — calling <code>pause()</code> on an
	already-paused engine is a no-op.
</p>

<pre><code>fluidRef.handle.pause();</code></pre>

<h3>resume()</h3>

<p>
	Restart the animation loop after a <code>pause()</code>. Idempotent — calling
	<code>resume()</code> on an already-running engine is a no-op.
</p>

<pre><code>fluidRef.handle.resume();</code></pre>

<h3>isPaused</h3>

<p>Readonly boolean property indicating whether the engine's animation loop is currently paused.</p>

<pre><code>if (fluidRef.handle.isPaused) {LB}
  fluidRef.handle.resume();
{RB}</code></pre>

